//串口函数解析——获取Gps中的utc时间
void extractUTC(const uint8_t *input, char *output) {
    // 找到第一个逗号的位置
    const char *ptr = strchr((const char *)input, ',');
    if (ptr != NULL) {
        // 将指针移动到下一个字符，即UTC时间的开始
        ptr++;
        // 找到小数点的位置，即UTC时间的整数部分结束
        const char *end = strchr(ptr, '.');
        if (end != NULL) {
            // 计算UTC时间整数部分的长度
            int length = end - ptr;
            // 确保长度为14个字符
            if (length == 14) {
                // 复制UTC时间整数部分到输出数组
                strncpy(output, ptr, length);
                // 确保输出字符串以空字符结尾
                output[length] = '\0';
            }
        }
    }
}


// 判断是否是闰年
int is_leap_year(int year) {
    return (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0);
}

// 获取每个月的天数
int get_days_in_month(int year, int month)
{
    int days_in_month[] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
    if (month == 2 && is_leap_year(year)) {
        return 29; // 闰年的二月有29天
    }
    return days_in_month[month - 1];
}

// 增加UTC时间指定的秒数
void increment_utc_time(char *utc, int add_seconds) {
    int year, month, day, hour, min, sec;

    // 解析时间字符串
    sscanf(utc, "%4d%2d%2d%2d%2d%2d", &year, &month, &day, &hour, &min, &sec);

    // 增加指定的秒数
    sec += add_seconds;

    // 处理进位
    while (sec >= 60) {
        sec -= 60;
        min++;
        if (min >= 60) {
            min -= 60;
            hour++;
            if (hour >= 24) {
                hour -= 24;
                day++;
                if (day > get_days_in_month(year, month)) {
                    day = 1;
                    month++;
                    if (month > 12) {
                        month = 1;
                        year++;
                    }
                }
            }
        }
    }

    // 将更新后的时间写回字符串
    sprintf(utc, "%04d%02d%02d%02d%02d%02d", year, month, day, hour, min, sec);
}
// 比较两个时间字符串并返回时间差（以秒为单位）
long compareTime(const char *time1, const char *time2) {
    struct tm tm1, tm2;
    memset(&tm1, 0, sizeof(tm1));
    memset(&tm2, 0, sizeof(tm2));
    sscanf(time1, "%4d%2d%2d%2d%2d%2d", &tm1.tm_year, &tm1.tm_mon, &tm1.tm_mday, &tm1.tm_hour, &tm1.tm_min, &tm1.tm_sec);
    sscanf(time2, "%4d%2d%2d%2d%2d%2d", &tm2.tm_year, &tm2.tm_mon, &tm2.tm_mday, &tm2.tm_hour, &tm2.tm_min, &tm2.tm_sec);

    tm1.tm_year -= 1900;
    tm1.tm_mon -= 1;
    tm2.tm_year -= 1900;
    tm2.tm_mon -= 1;

    time_t t1 = mktime(&tm1);
    time_t t2 = mktime(&tm2);

    return difftime(t1, t2);
}

